/*
-------------------------------------------------------
إعدادات المؤقت الدائري
-------------------------------------------------------

TOTAL
إجمالي الوقت للسؤال بالثواني

R
نصف قطر الدائرة التي يتم رسم المؤقت عليها

CIRCUMFERENCE
محيط الدائرة
نستخدمه لحساب طول القوس الذي يمثل التقدم
*/

const TOTAL = 10
const R = 16
const CIRCUMFERENCE = 2 * Math.PI * R


/**
 * CountdownTimer
 *
 * كومبوننت يعرض مؤقت دائري تنازلي للسؤال.
 *
 * الفكرة:
 * - يتم رسم دائرة SVG
 * - جزء منها يمثل الوقت المتبقي
 * - كل ثانية يقل القوس حتى يصل للصفر
 *
 * @param {number} timeLeft
 * الوقت المتبقي للسؤال بالثواني
 */

export function CountdownTimer({ timeLeft }) {

  /*
  -------------------------------------------------------
  حساب نسبة التقدم
  -------------------------------------------------------
  مثال:
  إذا بقي 5 ثواني من 10
  progress = 0.5
  */

  const progress = timeLeft / TOTAL


  /*
  -------------------------------------------------------
  حساب مقدار تحرك القوس
  -------------------------------------------------------

  strokeDashoffset يتحكم في مقدار إخفاء القوس.

  عندما:
  progress = 1 → القوس كامل
  progress = 0 → القوس مخفي
  */

  const dashOffset = CIRCUMFERENCE * (1 - progress)


  /*
  -------------------------------------------------------
  حالة الاستعجال
  -------------------------------------------------------

  عندما يتبقى 3 ثواني أو أقل
  يتحول لون المؤقت إلى الأحمر
  */

  const isUrgent = timeLeft <= 3


  return (

    /*
    حاوية المؤقت
    */

    <div className="timer-wrap">

      {/*
      SVG يستخدم لرسم المؤقت الدائري
      */}

      <svg width="44" height="44" viewBox="0 0 44 44">

        {/*
        -------------------------------------------------------
        الدائرة الخلفية (Track)
        -------------------------------------------------------
        تمثل المسار الكامل للمؤقت
        */}

        <circle
          cx="22"
          cy="22"
          r={R}
          fill="none"
          stroke="var(--border)"
          strokeWidth="2.5"
        />


        {/*
        -------------------------------------------------------
        قوس التقدم (Progress Arc)
        -------------------------------------------------------

        هذا هو الجزء المتحرك الذي يقل مع مرور الوقت
        */}

        <circle
          cx="22"
          cy="22"
          r={R}
          fill="none"

          // اللون يتغير إذا الوقت أوشك على الانتهاء
          stroke={isUrgent ? 'var(--red)' : 'var(--accent)'}

          strokeWidth="2.5"
          strokeLinecap="round"

          // طول القوس الكلي
          strokeDasharray={CIRCUMFERENCE}

          // مقدار الإخفاء حسب الوقت المتبقي
          strokeDashoffset={dashOffset}

          // تدوير الدائرة ليبدأ المؤقت من الأعلى
          transform="rotate(-90 22 22)"

          // حركة ناعمة عند تغير الوقت
          style={{
            transition: 'stroke-dashoffset 0.9s linear, stroke 0.3s ease'
          }}
        />


        {/*
        -------------------------------------------------------
        الرقم داخل المؤقت
        -------------------------------------------------------
        يعرض عدد الثواني المتبقية
        */}

        <text
          x="22"
          y="22"
          dominantBaseline="central"
          textAnchor="middle"
          fontSize="12"
          fontFamily="var(--font-mono)"
          fontWeight="500"

          // يتغير اللون عند قرب انتهاء الوقت
          fill={isUrgent ? 'var(--red)' : 'var(--text-primary)'}
        >
          {timeLeft}
        </text>

      </svg>

    </div>
  )
}