/**
 * -------------------------------------------------------
 * حساب النقاط للسؤال
 * -------------------------------------------------------
 *
 * الفكرة الأساسية:
 * كلما أجاب اللاعب مبكراً (قبل ظهور كلمات كثيرة من السؤال)
 * يحصل على نقاط أعلى.
 *
 * مثال:
 * إذا أجاب عند ظهور الكلمة الأولى → يحصل على أعلى نقاط.
 * إذا أجاب بعد ظهور معظم كلمات السؤال → يحصل على نقاط أقل.
 *
 * @param {number} wordRevealedAt
 * رقم الكلمة التي كانت ظاهرة عندما أجاب اللاعب.
 * (الترقيم يبدأ من 1)
 *
 * مثال:
 * إذا أجاب عندما ظهرت الكلمة الثالثة → القيمة = 3
 *
 * @param {number} totalWords
 * العدد الكلي لكلمات السؤال.
 *
 * @param {number} maxScore
 * أعلى عدد نقاط ممكن يحصل عليها اللاعب في السؤال.
 * القيمة الافتراضية = 100
 *
 * @returns {number}
 * عدد النقاط النهائي بين:
 * minScore و maxScore
 */

export function calculateScore(wordRevealedAt, totalWords, maxScore = 100) {

  /*
  الخصم: لكل كلمة يتم كشفها تنقص 10 نقاط
  */
  const penaltyPerWord = 10

  /*
  حساب مجموع النقاط المخصومة بناءً على ترتيب الكلمة
  (الكلمة الأولى لا يوجد عليها خصم)
  */
  const penalty = (wordRevealedAt - 1) * penaltyPerWord

  /*
  حساب النتيجة النهائية
  */
  let finalScore = maxScore - penalty

  /*
  حتى لو كان السؤال طويلا جدا (أكثر من 10 كلمات)،
  أقل نقاط ممكن أن يحصل عليها اللاعب للإجابة الصحيحة هي 10 نقاط.
  */
  if (finalScore < 10) {
    finalScore = 10
  }

  return finalScore
}


/**
 * -------------------------------------------------------
 * تنسيق النقاط للعرض
 * -------------------------------------------------------
 *
 * هذه الدالة تستخدم عند عرض النقاط في الواجهة.
 *
 * toLocaleString تقوم بتنسيق الأرقام لتكون مقروءة أكثر.
 *
 * مثال:
 * 1000 → 1,000
 * 1000000 → 1,000,000
 */

export function formatScore(score) {
  return score.toLocaleString()
}